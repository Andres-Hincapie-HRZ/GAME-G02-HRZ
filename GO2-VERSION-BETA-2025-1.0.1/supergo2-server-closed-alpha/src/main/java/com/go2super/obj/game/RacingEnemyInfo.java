package com.go2super.obj.game;

import com.go2super.buffer.Go2Buffer;
import com.go2super.obj.BufferObject;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@Builder
@AllArgsConstructor
@NoArgsConstructor
public class RacingEnemyInfo extends BufferObject {

    public long userId;
    private int rankId;
    private int gameServerId;
    private String username;

    @Override
    public void write(Go2Buffer go2buffer) {

        go2buffer.pushByte((8 - go2buffer.getBuffer().position() % 8) % 8);

        go2buffer.addLong(userId);
        go2buffer.addUnsignInt(rankId);
        go2buffer.addInt(gameServerId);
        go2buffer.addString(username, 32);

    }

    @Override
    public RacingEnemyInfo trash() {
        return new RacingEnemyInfo();
    }

}